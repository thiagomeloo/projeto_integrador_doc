
import java.util.ArrayList;



public class Restaurante {
    
    private String nome;
    private String cnpj;
    private String regiao;

    public Restaurante(String nome, String cnpj, String regiao) {
        this.nome = nome;
        this.cnpj = cnpj;
        this.regiao = regiao;
    }

    public String getNome() {
        return nome;
    }

    public String getCnpj() {
        return cnpj;
    }

    public String getRegiao() {
        return regiao;
    }

    
        
    

   
   
    
    
}
